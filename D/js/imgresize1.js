    function heightpx() {
    let hpix = document.getElementById("hpx");
    let image = document.getElementById("img");
    let value = hpix.value + "px"
    image.style.height = value;
    }
    function heightper() {
    let hper = document.getElementById("hpercent");
    let image = document.getElementById("img");
    let value = hper.value + "%"
    image.style.height = value;
    }
